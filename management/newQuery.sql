SELECT COUNT(ID), lat, lng FROM
(SELECT ID, 40.85251385000002 as lat, -73.82719840000003 as lng, PICKUP_TIME FROM NYCCAB.TRIP
WHERE DROPOFF_LONG < -73.76702290000003 AND DROPOFF_LONG > -73.79109310000004 AND DROPOFF_LAT < 40.86469600000002 AND DROPOFF_LAT > 40.84033170000002
	UNION ALL
SELECT ID, 40.65251385000002 as lat, -73.82719840000003 as lng, PICKUP_TIME FROM NYCCAB.TRIP
WHERE DROPOFF_LONG < -73.83923350000002 AND DROPOFF_LONG > -73.86330370000003 AND DROPOFF_LAT < 40.86469600000002 AND DROPOFF_LAT > 40.84033170000002)
WHERE PICKUP_TIME >= '2010-01-01' AND PICKUP_TIME <= '2011-04-13'
GROUP BY lat, lng