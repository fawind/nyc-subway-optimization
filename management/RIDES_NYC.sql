CREATE VIEW "NYCCAB"."RIDES_NYC" ( "ID",
	 "PICKUP_TIME",
	 "DROPOFF_TIME",
	 "PASSENGER",
	 "TRIPTIME",
	 "DISTANCE",
	 "PICKUP_LONG",
	 "PICKUP_LAT",
	 "DROPOFF_LONG",
	 "DROPOFF_LAT" ) AS select
	 "ID",
	 "PICKUP_TIME",
	 "DROPOFF_TIME",
	 LEAST(PASSENGER, 10) AS PASSENGER,
	 "TRIPTIME",
	 "DISTANCE",
	 "PICKUP_LONG",
	 "PICKUP_LAT",
	 "DROPOFF_LONG",
	 "DROPOFF_LAT" 
from "NYCCAB"."TRIP"
WHERE DROPOFF_LONG < -73.689058 AND DROPOFF_LONG > -74.079760
AND DROPOFF_LAT < 40.924695 AND DROPOFF_LAT > 40.561053
AND PICKUP_LONG < -73.892477 AND PICKUP_LONG > -74.021277
AND PICKUP_LAT < 40.876428 AND PICKUP_LAT > 40.700279
WITH READ ONLY